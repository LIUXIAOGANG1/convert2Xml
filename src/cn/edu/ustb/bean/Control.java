package cn.edu.ustb.bean;

public class Control {
	public String name;
	public String x;
	public String y;

	public Control() {
		name = "";
		x = "";
		y = "";
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getX() {
		return x;
	}

	public void setX(String x) {
		this.x = x;
	}

	public String getY() {
		return y;
	}

	public void setY(String y) {
		this.y = y;
	}
}
